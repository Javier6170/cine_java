    private void btnOcuparActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnOcuparActionPerformed
        if (txtNombre.getText().isEmpty() || txtApellido.getText().isEmpty() || txtCedula.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Debes colocar toda la informacion");
        } else {
            try {
                String nombre = txtNombre.getText();
                String apellido = txtApellido.getText();
                int documento = Integer.parseInt(txtCedula.getText());
                String nombrePelicula = String.valueOf(cmbNombrePelicula.getSelectedItem());
                String telefono = txtTelefono.getText();
                String duracion = txtDuracion.getText();
                Pelicula pelicula = new Pelicula(nombrePelicula, duracion);
                Persona persona = new Persona(nombre, apellido, documento, pelicula, telefono, 0, 0, null);
                vistaPrincipal.controladorCRUDCine.agregarPersona(persona, fila, columna);
                JOptionPane.showMessageDialog(null, "se ha ocupado!!!");
                
                
                vistaPrincipal.controladorCRUDCine.estadoOcupada(fila, columna);
                vistaPrincipal.validarPosiciones();
                vistaPrincipal.setEnabled(true);
                this.dispose();
            } catch (PersonaYaExisteException ex) {
                JOptionPane.showMessageDialog(null, ex.getMessage());
            }

        }
    }//GEN-LAST:event_btnOcuparActionPerformed

