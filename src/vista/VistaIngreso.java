    private void btnGuardarArgumentoDeshabiliarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarArgumentoDeshabiliarActionPerformed
        if (txtADeshabiliatSilla.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Debes colocar la razon por la cual se deshabilita la silla");
        } else {
            String razon = txtADeshabiliatSilla.getText();
            vistaPrincipal.controladorCRUDCine.razonPorDeshabilitacion(fila, columna, razon);
            JOptionPane.showMessageDialog(null, "Se ha deshabilitado por la razon");
            vistaPrincipal.validarPosiciones();
            vistaPrincipal.setEnabled(true);
            this.dispose();
        }

    }//GEN-LAST:event_btnGuardarArgumentoDeshabiliarActionPerformed

